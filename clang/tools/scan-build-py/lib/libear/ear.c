// skip the option in "<option  value>"
char *skip_option(char *working) {
  if (working == NULL) {
    return NULL;
  }
  int len = strlen(working);
  while (len > 0) {
    len--;
    if (isblank(*working)) {
      working++;
      break;
    } else {
      working++;
    }
  }
  if (len == 0) {
    return NULL;
  }
  return working;
}
// skip the value in "<option  value>"
char *skip_value(char *working) {
  if (working == NULL) {
    return NULL;
  }
  int len = strlen(working);
  int require_quotation = 0;
  while (len > 0) {
    len--;
    // process "
    if (require_quotation && *working != '"') {
      working++;
      continue;
    } else if (require_quotation && *working == '"') {
      require_quotation = 0;
      working++;
      continue;
    }
    if (*working == '"') {
      working++;
      require_quotation = 1;
      continue;
    }
    //
    if (isblank(*working)) {
      working++;
      break;
    } else {
      working++;
    }
  }
  if (len == 0) {
    return NULL;
  }
  return working;
}
